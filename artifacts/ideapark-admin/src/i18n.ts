export function parkingLabel(type: 'naziemne' | 'podziemne', lang: Lang) {
  if (type === 'podziemne') return lang === 'pl' ? '🅿️ Garaż podziemny' : '🅿️ Underground Garage';
  return lang === 'pl' ? '🚗 Naziemne' : '🚗 Surface';
}
export function parkingShort(type: 'naziemne' | 'podziemne', lang: Lang) {
  if (type === 'podziemne') return lang === 'pl' ? 'Podziemne' : 'Underground';
  return lang === 'pl' ? 'Naziemne' : 'Surface';
}
