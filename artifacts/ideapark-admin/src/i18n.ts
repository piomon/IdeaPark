export type Lang = 'pl' | 'en';
export type Theme = 'light' | 'dark' | 'beige';

const LANG_KEY = 'ideapark_lang';
const THEME_KEY = 'ideapark_theme';

export function loadLang(): Lang {
  return (localStorage.getItem(LANG_KEY) as Lang) || 'pl';
}
export function saveLang(l: Lang) { localStorage.setItem(LANG_KEY, l); }
export function loadTheme(): Theme {
  const raw = localStorage.getItem(THEME_KEY);
  if (raw === 'gray') { localStorage.setItem(THEME_KEY, 'beige'); return 'beige'; }
  if (raw === 'light' || raw === 'dark' || raw === 'beige') return raw;
  return 'light';
}
export function saveTheme(t: Theme) { localStorage.setItem(THEME_KEY, t); }

export function formatDate(iso: string, lang: Lang) {
  const d = new Date(iso);
  if (isNaN(d.getTime())) return '—';
  return new Intl.DateTimeFormat(lang === 'pl' ? 'pl-PL' : 'en-US', { day: 'numeric', month: 'short' }).format(d);
}

export function formatDateTime(iso: string, lang: Lang) {
  const d = new Date(iso);
  if (isNaN(d.getTime())) return '—';
  const date = new Intl.DateTimeFormat(lang === 'pl' ? 'pl-PL' : 'en-US', { day: 'numeric', month: 'short' }).format(d);
  const time = d.toLocaleTimeString(lang === 'pl' ? 'pl-PL' : 'en-US', { hour: '2-digit', minute: '2-digit', hour12: false });
  return `${date}, ${time}`;
}

export function formatTime(iso: string) {
  const d = new Date(iso);
  if (isNaN(d.getTime())) return '—';
  return d.toLocaleTimeString('pl-PL', { hour: '2-digit', minute: '2-digit', hour12: false });
}

export function timeAgo(iso: string, lang: Lang) {
  const h = Math.floor((Date.now() - new Date(iso).getTime()) / 3600000);
  if (h < 1) return lang === 'pl' ? 'teraz' : 'now';
  if (h < 24) return `${h}h`;
  return `${Math.floor(h / 24)}d`;
}

